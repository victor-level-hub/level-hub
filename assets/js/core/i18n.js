// Helper global de i18n p/ strings geradas em JS (toasts, modais LevelModal,
// validações) — lê do dict central pela língua ativa. Use tHub('chave') com a
// chave PT/EN definida em window.I18N. Suporta {placeholder} via .replace no call site.
window.tHub = function (key, fb) {
  try {
    var lang = (localStorage.getItem('bo7hub_lang_v1') === 'en') ? 'en' : 'pt';
    var d = window.I18N && window.I18N[lang];
    if (d && d[key] != null) return d[key];
  } catch (e) {}
  return (fb != null) ? fb : key;
};

window.applyI18N = function(lang) {
  const dict = window.I18N[lang] || window.I18N.pt;
  document.documentElement.lang = (lang === 'pt') ? 'pt-BR' : 'en';

  // textContent
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      var v = dict[key];
      if (window.hasIconPlaceholder && window.hasIconPlaceholder(v)) {
        el.innerHTML = window.renderIconsText(v);
      } else {
        el.textContent = v;
      }
    }
  });

  // innerHTML (only for keys with inline HTML)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) {
      var v = dict[key];
      el.innerHTML = (window.renderIconsHtml ? window.renderIconsHtml(v) : v);
    }
  });

  // attributes (format: "attr:key" or "attr1:key1|attr2:key2")
  document.querySelectorAll('[data-i18n-attr]').forEach(el => {
    const spec = el.getAttribute('data-i18n-attr');
    spec.split('|').forEach(pair => {
      const idx = pair.indexOf(':');
      if (idx < 1) return;
      const attr = pair.slice(0, idx).trim();
      const key  = pair.slice(idx + 1).trim();
      if (attr && dict[key] !== undefined) el.setAttribute(attr, dict[key]);
    });
  });

  // Update toggle button active state
  document.querySelectorAll('.lang-toggle .lang-opt').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
};

window.setLanguage = function(lang) {
  if (lang !== 'pt' && lang !== 'en') lang = 'pt';
  try { localStorage.setItem('bo7hub_lang_v1', lang); } catch (e) {}
  window.applyI18N(lang);
  // Hook for downstream modules (e.g. dynamic content rerender) — Phase B+
  window.dispatchEvent(new CustomEvent('languagechange', { detail: { lang } }));
  // Re-sincroniza marcação do switch técnico (o efetivo pode mudar se estiver em 'auto')
  try { window.syncTechLangToggle && window.syncTechLangToggle(); } catch (e) {}
};

// ── Linguagem Técnica (v2.11.0) — idioma das traduções dos termos do jogo ──
// Delega ao módulo LevelGlossary (que persiste em ui_prefs e re-traduz as zonas).
// ── Termos técnicos por-chave (nomes de config + abas) — v2.53.2 ──
// Elementos [data-techterm="<chave I18N>"] mostram o texto na LINGUAGEM DOS TERMOS
// TÉCNICOS (o que o jogador vê no console do PS5), independente da língua de login.
// Regra: nome da config + rótulo da aba = termo técnico; descrição/explicação = língua de login.
window.applyTechTerms = function () {
  var tl = 'pt';
  try {
    var p = JSON.parse(localStorage.getItem('bo7hub_ui_prefs_v1') || '{}');
    var t = p.tech_lang || 'auto';
    var hub = (localStorage.getItem('bo7hub_lang_v1') === 'en') ? 'en' : 'pt';
    tl = (t === 'auto') ? hub : (t === 'en' ? 'en' : 'pt');
  } catch (e) {}
  var dict = (window.I18N && window.I18N[tl]) || (window.I18N && window.I18N.pt) || {};
  document.querySelectorAll('[data-techterm]').forEach(function (el) {
    var k = el.getAttribute('data-techterm');
    var v = dict[k];
    if (v != null) el.textContent = v;
  });
};
window.addEventListener('languagechange', function () { try { window.applyTechTerms(); } catch (e) {} });

window.setTechLang = function(lang) {
  if (lang !== 'pt' && lang !== 'en') return;
  if (window.LevelGlossary && typeof window.LevelGlossary.setTechLang === 'function') {
    window.LevelGlossary.setTechLang(lang);
  } else {
    // Módulo ainda não carregou: persiste direto pra não perder a escolha
    try {
      const p = JSON.parse(localStorage.getItem('bo7hub_ui_prefs_v1') || '{}');
      p.tech_lang = lang;
      localStorage.setItem('bo7hub_ui_prefs_v1', JSON.stringify(p));
    } catch (e) {}
  }
  window.syncTechLangToggle();
  try { window.applyTechTerms(); } catch (e) {}
};

// Marca o botão ativo do switch técnico conforme o idioma EFETIVO dos termos.
window.syncTechLangToggle = function() {
  let eff = 'pt';
  try {
    eff = (window.LevelGlossary && window.LevelGlossary.effectiveTechLang)
      ? window.LevelGlossary.effectiveTechLang()
      : (JSON.parse(localStorage.getItem('bo7hub_ui_prefs_v1')||'{}').tech_lang
         || (localStorage.getItem('bo7hub_lang_v1') === 'en' ? 'en' : 'pt'));
  } catch (e) {}
  document.querySelectorAll('.techlang-toggle .lang-opt').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-techlang') === eff);
  });
};
document.addEventListener('DOMContentLoaded', function () {
  setTimeout(function () { try { window.syncTechLangToggle(); } catch (e) {} try { window.applyTechTerms(); } catch (e) {} }, 300);
});

/* ===== HELP POPOVER controller (v2.12.1) =====
   Hover/focus já é tratado por CSS. Este controller só adiciona o modo
   clique/toque (alterna aberto/fechado), fecha ao clicar fora ou Escape,
   e garante um único popover aberto por vez. */
(function initHelpPop() {
  function closeAll(except) {
    document.querySelectorAll('.help-pop.is-open').forEach(function (el) {
      if (el !== except) el.classList.remove('is-open');
    });
  }
  // Toggle no botão: usa pointerup direto no elemento clicado, com guarda de
  // tempo pra absorver eventos duplicados (path + button na mesma interação).
  var lastToggle = 0;
  function onBtn(e) {
    var btn = e.target && e.target.closest ? e.target.closest('.help-pop-btn') : null;
    if (!btn) return;
    var now = Date.now();
    if (now - lastToggle < 60) { e.preventDefault(); e.stopPropagation(); return; }
    lastToggle = now;
    var pop = btn.closest('.help-pop');
    if (!pop) return;
    e.preventDefault();
    e.stopPropagation();
    var willOpen = !pop.classList.contains('is-open');
    closeAll(pop);
    pop.classList.toggle('is-open', willOpen);
  }
  document.addEventListener('click', onBtn, true); // capture: roda antes de qualquer close
  // Fecha ao clicar/tocar fora de qualquer help-pop (delegado, fase bubble).
  document.addEventListener('pointerdown', function (e) {
    if (e.target && e.target.closest && e.target.closest('.help-pop')) return;
    closeAll(null);
  });
  document.addEventListener('keydown', function (e) {
    if (e.key === 'Escape') closeAll(null);
  });
})();

(function initI18N() {
  let saved = 'pt';
  try { saved = localStorage.getItem('bo7hub_lang_v1') || 'pt'; } catch (e) {}
  if (saved !== 'pt' && saved !== 'en') saved = 'pt';
  // Apply ASAP if DOM ready, else defer
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => window.applyI18N(saved));
  } else {
    window.applyI18N(saved);
  }
  // Set initial toggle active state even before applyI18N runs
  document.addEventListener('DOMContentLoaded', () => {
    document.querySelectorAll('.lang-toggle .lang-opt').forEach(btn => {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === saved);
    });
  });
})();

/* ===== LANGUAGE popover controller (v2.41.0) =====
   Um botão discreto (globo + código) abre um popover com dois ajustes
   independentes: idioma do site (setLanguage) e termos técnicos (setTechLang).
   O marcador .active das opções é tratado por applyI18N/syncTechLangToggle. */
(function initLangPopover() {
  function el(id) { return document.getElementById(id); }
  function currentSiteLang() {
    try { return localStorage.getItem('bo7hub_lang_v1') === 'en' ? 'en' : 'pt'; } catch (e) { return 'pt'; }
  }
  function syncCode() {
    var code = el('lvl-lang-code');
    if (code) code.textContent = currentSiteLang().toUpperCase();
  }
  function open(btn, pop) { pop.removeAttribute('hidden'); btn.classList.add('open'); btn.setAttribute('aria-expanded', 'true'); }
  function close(btn, pop) { pop.setAttribute('hidden', ''); btn.classList.remove('open'); btn.setAttribute('aria-expanded', 'false'); }
  function init() {
    var btn = el('lvl-lang-btn'), pop = el('lvl-lang-pop');
    if (!btn || !pop) return;
    syncCode();
    btn.addEventListener('click', function (e) {
      e.stopPropagation();
      if (pop.hasAttribute('hidden')) open(btn, pop); else close(btn, pop);
    });
    document.addEventListener('click', function (e) {
      if (!e.target || !e.target.closest || !e.target.closest('#lvl-lang')) close(btn, pop);
    });
    document.addEventListener('keydown', function (e) { if (e.key === 'Escape') close(btn, pop); });
    // Trocar o idioma do site atualiza o código do botão (re-render via setLanguage→applyI18N)
    window.addEventListener('languagechange', syncCode);
  }
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else { init(); }
})();
